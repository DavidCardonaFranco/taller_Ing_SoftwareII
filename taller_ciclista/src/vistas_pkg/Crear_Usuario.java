    void crear_usuario(){
        String usuario = txt_usuario.getText();
        String clave = txt_clave.getText();
        String nombre = txt_nombre.getText();
        if(usuario.equals("") || clave.equals("") || nombre.equals("")){
             JOptionPane.showMessageDialog(this, "Favor de volver a registrarse.");
        }else{
            String sql = "INSERT INTO `tb_usuarios`(`usuario`, `clave`, `nombre`) VALUES ('" + usuario + "','" + clave + "','" + nombre + "');";
            try{
                cn=con.getConection();
                st=cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(this, "Usuario registrado.");
                this.setVisible(false);
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, e);
            
            }
        }
    }

